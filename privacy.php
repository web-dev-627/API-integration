</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">11. Særlig om markedsføring i e-post og SMS</h4>
</p>
<p>
    Dersom du har et aktivt kundeforhold til oss, kan vi sende deg
    markedsføring på e-post eller med andre elektroniske kommunikasjonsmetoder
    i henhold til markedsføringsloven § 15. Det inkluderer nyhetsbrev og andre
    henvendelser vedrørende innhold, tjenester, tilbud, kampanjer og
    arrangementer fra oss, via e-post, telefon, SMS og sosiale medier.
    <br/>
    <br/>
