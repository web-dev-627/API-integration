    markedsføring hvis du har gitt oss et samtykke til dette. Din persondata
    kan også brukes til å tilpasse denne kommunikasjonen. Du kan når som helst
    enkelt og kostnadsfritt melde deg av markedsføringshenvendelser gjennom
    avmeldings-funksjonen i e-postene/SMS-ene.
</p>
