<h3>
    <b>PERSONVERN</b>
</h3>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">1. Innledning</h4>
    Denne personvernerklæringen gjelder for Moss Dekk AS og forklarer hvorfor
    vi samler inn informasjon om deg, hvordan vi bruker denne informasjonen og
    hvordan vi tar hensyn til ditt personvern.
</p>
<p>
    Moss Dekk AS, ved daglig leder, er behandlingsansvarlig for virksomhetens
    behandling av personopplysninger. Erklæringen inneholder opplysninger du
    har krav på når det samles inn opplysninger fra nettstedene våre
    (personopplysningsloven § 19) og generell informasjon om hvordan vi
    behandler personopplysninger (personopplysningsloven § 18 1. ledd).
</p>
<p>
    Moss Dekk AS grunnlag for å behandle personopplysninger vil variere, men
    vil i henhold til personopplysningsloven § 8 bokstav a og b bestå av
    samtykke fra den registrerte eller fordi det er lovpålagt. Videre kan det
    være nødvendig å behandle personopplysninger for å oppfylle en avtale med
    den registrerte eller for å oppfylle en rettslig forpliktelse.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">2. Betaling med kort, faktura eller avbetaling</h4>
</p>
<p>
    Vi bruker Bambora som samarbeidspartner for å håndtere betaling. Enten du
    velger å betale med kort, faktura.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">3. Om personopplysninger og regelverket</h4>
</p>
<p>
    Personopplysninger er opplysninger og vurderinger som kan knyttes til en
    identifiserbar enkeltperson. Dette kan være navn, adresse, telefonnummer,
    e-postadresse, IP-adresse og kjøps- og adferds historikk.
</p>
<p>
    All behandling av personopplysninger, slik som innsamling, registrering,
    lagring og utlevering er underlagt særskilte regler, blant annet i
    personopplysningsloven. Det er daglig leder som er ansvarlig for at
    behandlingen skjer i samsvar med lovens regler. Datatilsynet fører tilsyn
    med at loven overholdes.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">4. Obligatorisk innsamling av personopplysninger</h4>
</p>
<p>
    Når du handler hos Moss Dekk trenger vi navn, adresse og e-post. Dette er
    nødvendig for å kunne registrere din bestilling og kontakte deg hvis det
    skulle være behov. Vi er pålagt å oppbevare informasjonen i forbindelse med
    regnskapsføring, avgifts håndtering og eventuell garanti-/returhåndtering.
    Denne historikken slettes ikke. Vi oppbevarer av sikkerhetshensyn også
    IP-adressen som er benyttet for å registrere bestillingen.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">5. Hva slags opplysninger samler vi inn?</h4>
</p>
<p>
    For å kunne levere så gode tjenester som mulig er vi avhengige av å samle
    inn ulike typer informasjon, inkludert personopplysninger om deg. Under
    følger en oversikt over hvordan vi typisk samler inn personopplysningene og
    hvilke opplysninger dette typisk er. Vi samler ikke inn sensitiv
    informasjon.
</p>
<p>
    <strong>a) Opplysninger du selv gir til oss</strong>
</p>
<p>
    Når du registrerer deg på vår nettside, må du oppgi en del informasjon som
    lagres av oss, slik som navn, e-postadresse og mobilnummer. Vi vil også
    lagre opplysninger når du kontakter oss senere. Informasjonen du oppgir kan
    også berikes ved hjelp av oppslagstjenester eller sosiale medier du gir oss
    tilgang til.
</p>
<p>
    <strong>b) Opplysninger vi får gjennom bruk av tjenestene våre</strong>
</p>
<p>
    Når du bruker våre tjenester, registrerer vi informasjon om hvilke
    tjenester du bruker og hvordan du bruker dem. Vi samler blant annet
    informasjon om:
</p>
<p>
    <strong>i) Din enhet og din internett-tilkobling</strong>
</p>
<p>
    Vi kan registrere informasjon om enheten du benytter, for eksempel
    produsent av mobil/PC, operativsystem og nettleser. Vi kan også samle
    informasjon om tilkoblingen til våre tjenester, som IP-adresser,
    nettverks-id, informasjonskapsler og unike identifikasjonsfiler.
</p>
<p>
    <strong>ii) Bruk av tjeneste eller kjøp</strong>
</p>
<p>
    Vi registrerer informasjon om din bruk av tjenestene, som hvilke sider du
    er inne på, når du er inne på sidene samt hvilke funksjoner du har brukt på
    sidene.
</p>
<p>
    <strong>c) Opplysninger vi får fra andre kilder</strong>
</p>
<p>
    Vi kan motta opplysninger om deg hvis du bruker noen av de andre tjenestene
    vi tilbyr på dette nettstedet. Vi samarbeider også tett med tredjeparter
    (f.eks. forretningspartnere, leverandører av tekniske tjenester,
    betalingstjenester og leveringstjenester, reklamenettverk,
    analyseselskaper, søkemotorer og kredittopplysningsbyråer) og kan motta
    opplysninger om deg fra disse. Dette inkluderer også opplysninger som er
    offentlig tilgjengelige.
</p>
<p>
    <strong>d) Informasjonskapsler og annet innhold som lagres lokalt</strong>
</p>
<p>
    Når du bruker våre tjenester eller er inne på våre nettsider, lagres
    informasjonskapsler og annen data som senere kan leses av oss.
    <br/>
    Mer om cookies lenger ned.
</p>
<p>
    <strong>e) Automatiske vurderinger</strong>
</p>
<p>
    Gjennom analyse av dine kjøp og atferd kan det utledes automatisk
    kategorisering av deg som kunde.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">6. Hva brukes opplysningene til?</h4>
</p>
<p>
    Vi bruker personopplysninger til følgende formål:
</p>
<p>
    <strong>a) For å levere og forbedre tjenestene våre</strong>
</p>
<p>
    Vi bruker personopplysninger for å levere tjenestene våre til deg. For
    eksempel trenger vi personopplysninger for at du skal kunne logge inn på
    våre sider og om relevant, for å kunne betale for tjenester. Vi bruker også
    personopplysninger for å sikre en best mulig brukeropplevelse for deg,
    blant annet ved å tilpasse visningen av innholdet til din skjerm/enhet og å
    sørge for raskest mulig lasting av sidene.
</p>
<p>
    <strong>
        b) For å tilpasse tjenestene, gi deg anbefalinger og relevant
        markedsføring
    </strong>
</p>
<p>
    Vi ønsker å gi deg anbefalinger, produktinformasjon og tjenestetilpasninger
    som er mest mulig relevant for deg. Dette vil både bli gitt på bakgrunn av
    din egen atferd, f.eks. på bakgrunn av hvilke produkter og tjenester du har
    brukt eller kjøpt, annonser du har klikket på, eller artikler du har lest,
    og på bakgrunn av atferden til andre brukere med liknende bruksmønster som
    deg.
</p>
<p>
    <strong>c) For å forbedre digital kommunikasjon og annonsering</strong>
</p>
<p>
    Vi bruker dine personopplysninger for å forbedre vår annonsering. Ved å
    opprette en profil hos oss godtar du målrettet annonsering gjennom
    programmatiske kjøp og målrettet annonsering gjennom sosiale medier der din
    persondata vil benyttes i kjøp av slik annonsering.
</p>
<p>
    <strong>d) For å utarbeide statistikk og forstå markedstrender</strong>
</p>
<p>
    Vi utarbeider statistikk og kartlegger markedstrender. Dette gjør vi for å
    kunne forbedre og videreutvikle produkttilbudene og tjenestene våre. Så
    langt det er praktisk mulig forsøker vi å gjøre dette med anonyme
    opplysninger, uten at vi vet at informasjonen er knyttet spesifikt til deg.
</p>
<p>
    <strong>e) For å forhindre misbruk av tjenestene våre</strong>
</p>
<p>
    Vi bruker personopplysninger for å forhindre misbruk av tjenestene våre.
    Misbruk kan være forsøk på å logge på andres kontoer, forsøk på svindel,
    “spamming”, hets, sjikane og andre handlinger som er forbudt etter norsk
    lov.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">7. Hva er cookies og hva brukes de til?</h4>
</p>
<p>
    Moss Dekk benytter informasjonskapsler (cookies), som de fleste andre
    nettbutikker, og i henhold til norsk lovgivning. En cookie er data som
    lagres i din nettleser for at nettsiden skal kjenne deg igjen fra side til
    side og besøk til besøk.
</p>
<p>
    <strong>a) Hva slags informasjon lagres?</strong>
</p>
<p>
    Moss Dekk bruker såkalte førsteparts-cookies for at du skal få en
    grunnleggende nettbutikksfunksjonalitet. Formålet med førsteparts-cookies
    er å kjenne igjen hvem du er slik at du får riktige priser og enklere finne
    frem til riktige varer, finne frem til tidligere kjøp, handlekurv etc.
</p>
<p>
    For at vi skal kunne tilpasse butikken til deg, bruker våre
    samarbeidspartnere tredjeparts-cookies for å spore dine bevegelser på våre
    sider. Moss Dekk bruker også opplysningene til å analysere din oppførsel
    for å gjøre nettbutikken enklere å bruke.
</p>
<p>
    Vi og våre samarbeidspartnere sporer annonser for at vi skal vite hvilke
    annonser du har sett på internett og hvilke produkter du har sett på hos
    oss, for å kunne tilpasse butikken etter dine interesser og vise deg
    relevante annonser når du går ut på internett igjen.
</p>
<p>
    <strong>b) Slik kan du unngå cookies</strong>
</p>
<p>
    Om du ønsker å unngå bruk av cookies kan du stille inn din nettleser til å
    ikke akseptere cookies automatisk. Se nettleserens hjelpesider for
    innstillinger. Vær oppmerksom på at om du velger å utelukke cookies, vil
    flere av funksjonene på mossdekk.no ikke fungere. Utenforstående kan bare
    få innsyn i opplysningene dersom dette er nødvendig for å utføre bestemte
    tjenester for moss dekk. Ved å bruke nettsiden mossdekk.no samtykker du til
    bruk av informasjonskapsler.
</p>
<p>
    <h4 style="color: #00b050; margin-top: 20px;">8. Deling av opplysninger</h4>
</p>
<p>
    Moss Dekk AS deler i noen tilfeller personopplysninger med andre selskaper
    som utfører tjenester på våre vegne. Dette er først og fremst for å gi deg
    en tryggere og bedre handleopplevelse. Her er de viktigste eksemplene:
</p>
<p>
    <strong>a) Når andre utfører tjenester på våre vegne</strong>
</p>
<p>
    For eksempel web- og markedsføringsbyråer til å drifte nettbutikken og vise
    deg målrettet markedsføring samt lage kampanjer. Disse har ikke lov til å
    bruke disse personopplysningene til noe annet enn å utføre tjenester for
    Moss Dekk AS.
</p>
<p>
    <strong>b) Ved mistanke om lovbrudd</strong>
</p>
<p>
    Vil informasjon kunne utleveres til offentlige myndigheter etter begjæring.
    Vi vil også kunne utlevere informasjon ved mistanke om svindel, eller
    informasjon som er nødvendig for å avklare konkrete tvister.
</p>
